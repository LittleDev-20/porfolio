function formatAge(birthdateISO) {
  if (!birthdateISO) return "";
  const birth = new Date(`${birthdateISO}T00:00:00`);
  if (Number.isNaN(birth.getTime())) return "";
  const now = new Date();
  let age = now.getFullYear() - birth.getFullYear();
  const beforeBirthdayThisYear =
    now.getMonth() < birth.getMonth() ||
    (now.getMonth() === birth.getMonth() && now.getDate() < birth.getDate());
  if (beforeBirthdayThisYear) age -= 1;
  return String(age);
}

function openLightbox({ title, images, startIndex }) {
  const list = (images || []).filter(Boolean);
  if (!list.length) return;

  let index = Math.max(0, Math.min(startIndex || 0, list.length - 1));

  const overlay = document.createElement("div");
  overlay.className = "lightbox";
  overlay.setAttribute("role", "dialog");
  overlay.setAttribute("aria-modal", "true");

  const panel = document.createElement("div");
  panel.className = "lightbox__panel";

  const top = document.createElement("div");
  top.className = "lightbox__top";

  const heading = document.createElement("div");
  heading.className = "lightbox__title";
  heading.textContent = title || "Screenshots";

  const close = document.createElement("button");
  close.type = "button";
  close.className = "btn btn--ghost";
  close.textContent = "Close";

  top.appendChild(heading);
  top.appendChild(close);

  const img = document.createElement("img");
  img.className = "lightbox__img";
  img.alt = title ? `${title} screenshot` : "Project screenshot";

  const controls = document.createElement("div");
  controls.className = "lightbox__controls";

  const prev = document.createElement("button");
  prev.type = "button";
  prev.className = "btn btn--ghost";
  prev.textContent = "Prev";

  const counter = document.createElement("div");
  counter.className = "lightbox__counter muted";

  const next = document.createElement("button");
  next.type = "button";
  next.className = "btn btn--ghost";
  next.textContent = "Next";

  controls.appendChild(prev);
  controls.appendChild(counter);
  controls.appendChild(next);

  function render() {
    img.src = list[index];
    counter.textContent = `${index + 1} / ${list.length}`;
    prev.disabled = index === 0;
    next.disabled = index === list.length - 1;
  }

  function cleanup() {
    document.removeEventListener("keydown", onKeyDown);
    overlay.remove();
  }

  function onKeyDown(e) {
    if (e.key === "Escape") cleanup();
    if (e.key === "ArrowLeft" && index > 0) {
      index -= 1;
      render();
    }
    if (e.key === "ArrowRight" && index < list.length - 1) {
      index += 1;
      render();
    }
  }

  close.addEventListener("click", cleanup);
  overlay.addEventListener("click", (e) => {
    if (e.target === overlay) cleanup();
  });

  prev.addEventListener("click", () => {
    if (index === 0) return;
    index -= 1;
    render();
  });
  next.addEventListener("click", () => {
    if (index >= list.length - 1) return;
    index += 1;
    render();
  });

  panel.appendChild(top);
  panel.appendChild(img);
  panel.appendChild(controls);
  overlay.appendChild(panel);

  document.body.appendChild(overlay);
  document.addEventListener("keydown", onKeyDown);
  render();

  close.focus();
}

