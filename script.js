function $(id) {
  const element = document.getElementById(id);
  if (!element) throw new Error(`Missing element: #${id}`);
  return element;
}

function setText(id, value) {
  $(id).textContent = value ?? "";
}

function setLink(id, href, fallbackHref = "#") {
  const a = $(id);
  const hasHref = Boolean(href && href.trim());
  a.href = hasHref ? href : fallbackHref;
  if (!hasHref) a.setAttribute("aria-disabled", "true");
}

function createTag(label, { active = false, onClick } = {}) {
  const el = document.createElement("button");
  el.type = "button";
  el.className = `tag${active ? " tag--active" : ""}`;
  el.textContent = label;
  if (onClick) el.addEventListener("click", onClick);
  return el;
}

function createButton(label, href, isPrimary = false) {
  const a = document.createElement("a");
  a.className = `btn ${isPrimary ? "btn--primary" : "btn--ghost"}`;
  a.textContent = label;
  a.href = href;
  a.target = "_blank";
  a.rel = "noreferrer";
  return a;
}

